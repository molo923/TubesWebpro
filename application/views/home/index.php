    <section class="ftco-section ftco-slant ftco-slant-light  bg-light ftco-slant ftco-slant-white" id="section-help">
      <div class="container">
        <div class="row mb-5">
          <div class="col-md-12 text-center ftco-animate">
            <h2 class="text-uppercase ftco-uppercase">Help</h2>
            <div class="row justify-content-center mb-5">
              <div class="col-md-7">
                <p class="lead">Apakah Anda masih bingung?</p>
              </div>
            </div>
          </div>
        </div>
        <!-- END row -->
        <div class="row">
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apa itu Bank Sampah?</h3>
            <p>Bank Sampah adalah suatu tempat yang digunakan untuk mengumpulkan sampah yang sudah dipilah-pilah. Hasil dari pengumpulan sampah yang sudah dipilah akan disetorkan ke tempat pembuatan kerajinan sampah atau ke tempat pengepul sampah. Bank Sampah dikelola menggunakan sistem seperti perbankan yang dilakukan oleh petugas sukarelawan. Penyetor adalah warga yang tinggal di sekitar lokasi bank serta mendapat buku tabungan seperti menabung di bank.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apa itu BankSampah.id?</h3>
            <p>BankSampah.id adalah aplikasi berbasis web & mobile yang bertujuan untuk mendukung kegiatan operasional Bank Sampah di seluruh Indonesia. BankSampah.id mengintegrasikan pengelolaan Bank Sampah secara nasional dan didukung aplikasi mobile yang dapat bekerja secara <i>offline</i>.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apa itu Smash?</h3>
            <p>Smash adalah singkatan dari Sistem Online Manajemen Sampah. Aplikasi ini kami berikan nama singkat agar mempermudah pengguna dalam mengingat nama aplikasi ini.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apa itu mySmash?</h3>
            <p>mySmash adalah aplikasi yang bertujuan untuk memberikan informasi mengenai Bank Sampah yang ada di sekitar Anda serta jenis sampah anorganik yang dapat dijual ke Bank Sampah tersebut.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apa yang dimaksud BankSampah.id Online dan BankSampah.id Mobile?</h3>
            <p>BankSampah.id Online adalah aplikasi berbasis web, yang dapat diakses di alamat <a href="www.banksampah.id">www.banksampah.id</a>. Sedangkan BankSampah.id Mobile adalah aplikasi berbasis Android, yang dapat diunduh pada <a href="https://play.google.com/store/apps/details?id=id.genetik.smash">Google Play Store</a> dengan kata kunci "BankSampah.id Mobile".</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Bagaimana cara melakukan registrasi Bank Sampah pada BankSampah.id?</h3>
            <p>Anda dapat mendaftarkan Bank Sampah Anda melalui dua cara, yaitu melalui registrasi di aplikasi BankSampah.id Online (<a href="www.banksampah.id/v2/register">www.banksampah.id/v2/register</a>) atau melalui registrasi di aplikasi BankSampah.id Mobile.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Saya sudah melakukan registrasi untuk Bank Sampah, apa hal selanjutnya yang harus saya lakukan?</h3>
            <p>Setelah Anda melakukan registrasi, Kami akan melakukan verifikasi data Bank Sampah terlebih dahulu. Setelah proses itu selesai, Kami akan memberikan pemberitahuan melalui alamat email dan nomor telepon yang Anda masukan pada form registrasi. Pada pemberitahuan tersebut Kami juga akan menginformasikan <b>username</b> dan <b>password</b> untuk Bank Sampah Anda, yang nantinya dapat digunakan untuk login pada BankSampah.id Online dan BankSampah.id Mobile.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Jika Saya lupa password, apa yang harus Saya lakukan?</h3>
            <p>Untuk memulihkan password Bank Sampah Anda, silahkan akses alamat <a href="www.banksampah.id/v2/forgot">www.banksampah.id/v2/forgot</a>. Pada form itu Anda cukup mengisi email yang digunakan pada saat registrasi dan tahap selanjutnya pemulihan password akan dikirimkan ke email Anda.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Saya sudah berhasil login pada BankSampah.id Online, apa hal selanjutnya yang dapat saya lakukan?</h3>
            <p>Setelah berhasil login, Anda dipersilahkan untuk memasukan kategori sampah dan harganya terlebih dahulu, di menu Kategori. Kategori sampah ini nantinya juga akan digunakan sebagai harga dasar untuk transaksi di BankSampah.id Mobile. Harga per kategori sampah juga dapat disesuaikan dengan masing - masing kebijakan/ketentuan harga Bank Sampah sesuai dengan perkembangan harga dari waktu ke waktu. Selain itu, Anda juga dapat melakukan pengaturan sistem bagi hasil dan minimum pengambilan saldo yang dapat dilakukan di menu Pengaturan.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apakah nasabah Bank Sampah dapat menggunakan aplikasi BankSampah.id Online dan BankSampah.id Mobile?</h3>
            <p>Untuk mengakses data yang dimiliki, nasabah bisa menggunakan aplikasi mySmash. Akses untuk nasabah dapat dikirimkan oleh Bank Sampah pada menu Nasabah & Transaksi.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apa kelebihan dari BankSampah.id Online?</h3>
            <p>BankSampah.id Online memiliki kelebihan dalam sisi tampilan data dan pengolahan informasi. Selain itu, melalui BankSampah.id Online, jumlah tampilan data yang dapat dikelola lebih banyak dibandingkan BankSampah.id Mobile, mengingat keterbatasan ukuran layar dari ponsel pintar.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apa kelebihan dari BankSampah.id Mobile?</h3>
            <p>BankSampah.id Mobile memiliki kelebihan utama dapat bekerja secara offline, yang memungkinkan Bank Sampah tidak harus terkoneksi pada internet setiap saat. Selain itu, dengan menggunakan BankSampah.id Mobile, petugas teller/pihak Bank Sampah tidak perlu repot â€“ repot membawa laptop atau komputer untuk melakukan transaksi, baik berupa kegiatan penyetoran sampah, maupun pengambilan saldo. Hal ini juga memudahkan Bank Sampah jika ingin melakukan kegiatan penabungan yang bersifat "Jemput Bola". Cukup dengan berbekal BankSampah.id Mobile pada ponsel pintar Anda, Bank Sampah dapat melakukan transaksi dimana saja dan kapan saja.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Apa yang dimaksud dengan transaksi offline?</h3>
            <p>Transaksi offline adalah transaksi baik berupa kegiatan penyetoran sampah ataupun pengambilan saldo, yang dilakukan melalui aplikasi BankSampah.id Mobile, pada kondisi koneksi internet yang kurang memadai atau tidak terdapat koneksi internet sama sekali.</p>
          </div>
          <div class="col-md-6 mb-5 ftco-animate">
            <h3 class="h5 mb-4">Bagaimana cara melakukan transaksi offline pada BankSampah.id Mobile?</h3>
            <p>Transaksi offline dapat dilakukan pada BankSampah.id Mobile dengan cara mengaktifkan pilihan "Simpan ke database smartphone" pada menu Pengaturan. Setelah diaktifkan, seluruh transaksi yang dilakukan melalui BankSampah.id Mobile akan ditampung sementara dan dapat dilihat rinciannya pada menu Histori Transaksi. Jika koneksi internet sudah memadai untuk melakukan sinkronisasi, Anda dapat menekan tombol Sync dan data transaksi yang dilakukan secara offline akan otomatis tersinkronisasi dengan server BankSampah.id Online.</p>
          </div>
        </div>
